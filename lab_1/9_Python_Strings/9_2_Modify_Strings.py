print("----------------------------------------------------------------------------------------\n")
print("The code data type 'Upper Case'\n")
print("The upper() method returns the string in upper case.\n")
print("Example for Upper Case:\n")
print('a = "Hello, World!"\n')
print('print(a.upper())\n')
print("The output\n")

# Upper case example:
a = "Hello, World!"
