"""
Python - Join Lists
"""
#-----------------------------------------------------
list1 = ["a", "b", "c"]
list2 = [1, 2, 3]

list3 = list1 + list2
print(list3)
#-----------------------------------------------------
lis = ["a", "b" , "c"]
lis = [1, 2, 3]

for x in lis:
  lis.append(x)

print(lis)
#-----------------------------------------------------
list11 = ["a", "b" , "c"]
list21 = [1, 2, 3]

list11.extend(list21)
print(list11)
