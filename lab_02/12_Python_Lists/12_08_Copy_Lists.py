"""
Python - Copy Lists
"""
#-----------------------------------------------------
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)
#-----------------------------------------------------
thislist1 = ["apple", "banana", "cherry"]
mylist1 = list(thislist1)
print(mylist1)
#-----------------------------------------------------
thislist2 = ["apple", "banana", "cherry"]
mylist2 = thislist2[:]
print(mylist2)
#-----------------------------------------------------