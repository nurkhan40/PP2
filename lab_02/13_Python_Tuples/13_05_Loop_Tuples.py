"""
Python - Loop Tuples
"""
#-----------------------------------------------------
thistuple = ("apple", "banana", "cherry")
for x in thistuple:
  print(x)
#-----------------------------------------------------
thistuple1 = ("apple", "banana", "cherry")
for i in range(len(thistuple1)):
  print(thistuple1[i])
#-----------------------------------------------------
thistuple2 = ("apple", "banana", "cherry")
i = 0
while i < len(thistuple2):
  print(thistuple2[i])
  i = i + 1
#-----------------------------------------------------