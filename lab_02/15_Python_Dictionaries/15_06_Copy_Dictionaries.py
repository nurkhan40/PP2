"""
Python - Copy Dictionaries
"""
#-----------------------------------------------------
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
mydict = thisdict.copy()
print(mydict)
#-----------------------------------------------------
thisdict1 = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
mydict1 = dict(thisdict1)
print(mydict1)
